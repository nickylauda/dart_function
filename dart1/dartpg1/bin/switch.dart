void main() {
  String sign = "/";
  switch (sign) {
    case "+":
      print("addition");
      break;
    case "-":
      print("substraction");
      break;
    case "*":
      print("multiplication");
      break;
    case "/":
      print("division");
      break;
    default:
      print("undefined");
      break;
  }
}
